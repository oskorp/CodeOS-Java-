

public class program2 {
    //type conversion or type conversion
    public static void main(String[] args){
        //Widening or Implicit conversion
        int x=5;
        double y=x;
        System.out.println(y);
        //Narrowing or Explicit conversion
        double a=23.45;
        int b=(int)a;
        System.out.print(b);


    }
}
